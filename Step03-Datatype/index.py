# int (integer)
age = 25       # Positive integer
temperature = -10  # Negative integer
count = 0      # Zero as an integer
print(age , temperature , count)      
    
# float (decimal)
pi = 3.14
print(pi)  

#Complex
z1 = 3 + 4j  
print(z1) 

# str (string)
name = "Code Queen"
print(name) 

# list
fruits = ["apple", "banana", "mango"]
print(fruits) 

# tuple
coordinates = (10, 20)
print(coordinates)  

# set
unique_numbers = {1, 2, 3, 3}
print(unique_numbers)  

#frozenset
vowels = frozenset("aeiou")  
print(vowels)  

# dict (dictionary)
person = {"name": "CodeQueen", "age": 17}
print(person)  

# bool (boolean)
is_active = True
print(is_active)  

# NoneType
value = None
print("NoneType:", value)  
