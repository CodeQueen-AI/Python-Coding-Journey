#1- Direct Indexing 
Fruits = ['apple', 'banana', 'cherry']
print(Fruits[2])

#2-Positive Indexing
Colors = ['red', 'green', 'blue']
print(Colors[0])

#3-Negative Indexing
Animals = ['cat', 'dog', 'elephant']
print(Animals[-2])

#4-Accessing a Loop
Fruits = ['apple', 'banana', 'cherry']
for fruit in Fruits:
    print(fruit)
    
#5-Accessing a range 
Fruits = ['apple', 'banana', 'cherry', 'date' , 'mango' , 'kiwi']
print(Fruits[1:5])  
