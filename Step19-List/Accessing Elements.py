# Direct Indexing
fruits = ["Apple", "Banana", "Cherry"]
print(fruits[0]) 
print(fruits[1])  
print(fruits[2])  

# Negative Indexing
fruits = ["Apple", "Banana", "Cherry"]
print(fruits[-1]) 
print(fruits[-2]) 
print(fruits[-3])  

# Positive Indexing 
fruits = ["Apple", "Banana", "Cherry", "Mango"]
print(fruits[0]) 
print(fruits[1])  
print(fruits[2])  
print(fruits[3]) 

# Accessing Using a Loop 
fruits = ["Apple", "Banana", "Cherry"]
for fruit in fruits:
    print(fruit)  

# Accessing a Range (Slicing) 
fruits = ["Apple", "Banana", "Cherry", "Mango", "Orange"]
print(fruits[1:4]) 
print(fruits[:3])   
print(fruits[2:])   
print(fruits[-3:-1]) 