#copy() methods
fruits = {"Apple", "Banana", "Cherry"}

# Looping through a set
for fruit in fruits:
    print(fruit)  

# Checking presence (Membership test)
if "Apple" in fruits:
    print("Apple is present!")
else:
    print("Apple is not present!")

# Checking absence
if "Mango" not in fruits:
    print("Mango is not present!")

