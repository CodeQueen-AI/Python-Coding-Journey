# Empty Set
my_set = set()  
print(my_set)

# Set with Elements
fruits = {"Apple", "Banana", "Cherry"}
print(fruits) 

# Set with Duplicate Values 
numbers = {1, 2, 2, 3, 4, 4, 5}
print(numbers)  

# Creating Set from a List 
my_list = [1, 2, 2, 3, 4]
unique_set = set(my_list)
print(unique_set)  

# Creating Set from a String 
char_set = set("hello")
print(char_set) 
