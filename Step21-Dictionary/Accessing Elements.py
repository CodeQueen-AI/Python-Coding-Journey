# Accessing Value using Key
person = {"name": "CodeQueen", "age": 17, "city": "Karachi"}
print(person["name"]) 

# Using get() Method
print(person.get("age")) 
print(person.get("country")) 

# Accessing All Keys
print(person.keys())  

# Accessing All Values
print(person.values()) 

# Accessing All Key-Value Pairs
print(person.items())  
