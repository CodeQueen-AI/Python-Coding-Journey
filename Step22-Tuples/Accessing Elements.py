# Indexing
my_tuple = ("apple", "banana", "cherry", "date")
print(my_tuple[0])  
print(my_tuple[2])  

# Negative Indexing
print(my_tuple[-1])
print(my_tuple[-2])  

# Slicing
my_tuple = ("apple", "banana", "cherry", "date", "elderberry")

print(my_tuple[1:4])  
print(my_tuple[:3])  
print(my_tuple[2:])  
