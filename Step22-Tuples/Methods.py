# count()
my_tuple = (1, 2, 2, 3, 4, 2, 5)
print(my_tuple.count(2))

# index()
my_tuple = (10, 20, 30, 40, 50)
print(my_tuple.index(30))

# len()
my_tuple = (10, 20, 30)
print(len(my_tuple))

# sorted()
my_tuple = (5, 3, 8, 1)
sorted_tuple = tuple(sorted(my_tuple))
print(sorted_tuple) 


# min()
my_tuple = (10, 20, 5, 40)
print(min(my_tuple)) 

# max()
print(max(my_tuple)) 

# sum()
my_tuple = (10, 20, 30)
print(sum(my_tuple))  
