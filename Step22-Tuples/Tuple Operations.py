# Concatenation
tuple1 = (1, 2, 3)
tuple2 = (4, 5, 6)
new_tuple = tuple1 + tuple2
print(new_tuple) 

# Single Element Handling
single_tuple = (5,)  
not_a_tuple = (5)   
print(type(single_tuple))
print(type(not_a_tuple))   

# Convert Tuple to List
my_tuple = (1, 2, 3)
my_list = list(my_tuple)  
my_list.append(4)        
print(my_list)          
