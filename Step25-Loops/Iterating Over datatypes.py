#String Iteration
for data in "Python":
    print(data)

#List Iteration
for item in ["Apple", "Banana", "Cherry"]:
    print(item)

# Tuple Iteration
for num in (10, 20, 30):
    print(num)

#Dictionary Iteration
for key, value in {"name": "Ali", "age": 20}.items():
    print(key, value)

# Set Iteration
for num in {5, 10, 15}:
    print(num)
